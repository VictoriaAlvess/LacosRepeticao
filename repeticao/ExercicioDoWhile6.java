package lacos.repeticao;

import java.util.Scanner;

/*
 * Escrever um programa que receba vários números inteiros no teclado.E no
final imprimir a média dos números múltiplos de 3. 
Para sair digitar(zero)
 */
public class ExercicioDoWhile6 {

	public static void main(String[] args) {
		Scanner ler = new Scanner(System.in);

		int i = 0, numero, media, soma = 0;

		do {
			System.out.println("Digite um número: ");
			numero = ler.nextInt();

			if (numero % 3 == 0) {
				soma += numero;
				i++;
			}
		} while (numero != 0);

		media = soma / i;

		System.out.println("A média dos números digitados é igual a: " + media);
	}
}
